    private void envioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_envioActionPerformed
        try{
            int me = Integer.parseInt(metros.getText());
            try{
                int cmt = Integer.parseInt(centimetros.getText());
                float cent = (float) cmt/100;
                float tamanho = (me)+cent;
                try{
                    int pesoC = Integer.parseInt(quilos.getText());
                    boolean sexoC = false;
                    if (feminino.isSelected())
                        sexoC = true;
                    else if (masculino.isSelected())
                        sexoC = false;
                    if (Integer.parseInt(centimetros.getText())>=100 || Integer.parseInt(centimetros.getText())<0){
                        JOptionPane.showMessageDialog(null, "Centimetros inválidos, digite um número entre 0 e 99");
                    }
                    else if(Integer.parseInt(metros.getText())>=3 || Integer.parseInt(metros.getText()) <0){
                        JOptionPane.showMessageDialog(null, "Metros inválidos, digite um número entre 0 e 2");
                    }
                    else if(Integer.parseInt(quilos.getText())<=0 ||Integer.parseInt(quilos.getText())>300 ){
                        JOptionPane.showMessageDialog(null, "Peso invalido, digite um valor entre 1 e 300");
                    }
                    else if (me==0 || cmt<10){
                        JOptionPane.showMessageDialog(null, "Tamanho Inválido");
                        JOptionPane.showMessageDialog(null, "Tamanho entre 0,10cm e 2m e 99cm");
                    }
                    else{
                        IMC imc = new IMC(tamanho, pesoC, sexoC);
                        TelaOutput tela = new TelaOutput(imc);
                        tela.setVisible(true);
                        dispose();
                    }
                }
                catch (NumberFormatException e){
                    JOptionPane.showMessageDialog(null, "Peso invalido, digite um valor acima de 0");
                }
            }
            catch (NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Centimetros inválidos, digite um número entre 0 e 99");
            }
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Metros inválidos, digite um número entre 0 e 2");
        }
        
    }//GEN-LAST:event_envioActionPerformed

